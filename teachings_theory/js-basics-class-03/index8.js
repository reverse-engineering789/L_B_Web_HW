// removing element

const numbers = [1,2,3,4,5,6,7,8];
// end
numbers.pop();
console.log(numbers);
// [
//     1, 2, 3, 4,
//     5, 6, 7
//   ]



// begining
const numbers2 = [1,2,3,4,5,6,7,8];
numbers2.shift();
console.log(numbers2);
// [
//     2, 3, 4, 5,
//     6, 7, 8
//   ]


// middle
const numbers3 = [1,2,3,4,5,6,7,8];
numbers3.splice(2, 1);
console.log(numbers3);
// [
//     1, 2, 4, 5,
//     6, 7, 8
