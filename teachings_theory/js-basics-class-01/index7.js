// comparison operators


console.log(5 > 3);
// Output:
// true


console.log(5 < 3);
// Output:
// false

console.log(5 >= 3);
// Output:
// true

console.log(5 <= 3);
// Output:
// false

console.log(5 === 3);
// Output:
// false

console.log(5 !== 3);
// Output:
// true


